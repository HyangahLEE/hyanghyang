package days01;

public class Ex04 {

	public static void main(String[] args) {
		// 연산자(operator)
		// 숫자(정수) + 숫자(정수) =         덧셈연산자 +
		 System.out.println(4 + 5);  // 9  숫자(정수) 
		 
		 // 문자열 연결 연산자 +
		 //                       문자열 4 + 정수(5)
		 System.out.println("4" + 5);  // "45" 문자열 45
		 
		 // Tab 키  '\t'
		 System.out.println("홍\t길" + "\r\n동");  // "홍길동"
		 
		 //  개행(줄바꿈)
		 // 안녕하세요 Enter
		 

	}

}
